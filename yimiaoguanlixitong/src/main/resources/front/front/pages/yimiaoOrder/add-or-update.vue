            // },


			// 日期控件
			yimiaoOrderTimeConfirm(val) {
                this.ruleForm.yimiaoOrderTime = val.result;
                this.$forceUpdate();
            },
			// 日期控件
			insertTimeConfirm(val) {
                this.ruleForm.insertTime = val.result;
                this.$forceUpdate();
            },
			// 日期控件
			createTimeConfirm(val) {
                this.ruleForm.createTime = val.result;
                this.$forceUpdate();
            },




            getUUID() {
                return new Date().getTime();
            },
            async onSubmitTap() {
				if ((!this.ruleForm.yimiaoOrderTime)) {
                    this.$utils.msg(`预约时间不能为空`);
                    return
                }
				if ((!this.ruleForm.yimiaoDidianTypes)) {
                    this.$utils.msg(`接种地点不能为空`);
                    return
                }
                if (this.ruleForm.id) {
                    await this.$api.update(`yimiaoOrder`, this.ruleForm);
                } else {
                    await this.$api.save(`yimiaoOrder`, this.ruleForm);
                }
                uni.setStorageSync('pingluenStateState', true);
                this.$utils.msgBack('提交成功');
            },
            getDate(type) {
                const date = new Date();
                let year = date.getFullYear();
                let month = date.getMonth() + 1;
                let day = date.getDate();
                if (type === 'start') {
                    year = year - 60;
                } else if (type === 'end') {
                    year = year + 2;
                }
                month = month > 9 ? month : '0' + month;;
                day = day > 9 ? day : '0' + day;
                return `${year}-${month}-${day}`;
            },
            toggleTab(str) {
                this.$refs[str].show();
            }
        }
